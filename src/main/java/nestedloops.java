import java.util.Scanner;

public class nestedloops {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);

        int i = 1;
        int k = 1;


        while(i <= 5) {
            while(5 >= k) {
                System.out.print(i + " ");
                i++;
                k++;


            }

        }


    }
}
